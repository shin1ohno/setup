# Sync binary + ad-hoc sign + reload launchd whenever bin_path is newer than
# the bundled copy (same pattern as edge-agent cookbook). `-nt` is true when
# the right-hand side is *not newer*, so `not_if` fires when the bundle is
# already up to date. The unload tolerates "not currently loaded" (first run
# before interactive bootstrap) via `2>/dev/null || true`; load is only
# reached if cp + codesign succeeded so a failing codesign leaves the old
# binary + launchd state intact. Declared AFTER the launchd_plist file so
# first-run ordering works (the plist must exist before launchctl load).
execute "sync MacosHub.app binary, codesign, and reload launchd" do
  command "cp -f #{bin_path} #{bundle_exec} && " \
          "codesign --force --deep --sign - #{app_bundle} && " \
          "{ launchctl unload #{launchd_plist} 2>/dev/null || true; } && " \
          "launchctl load #{launchd_plist}"
  user user
  only_if "test -x #{bin_path}"
  not_if "test -x #{bundle_exec} && ! [ #{bin_path} -nt #{bundle_exec} ]"
end
