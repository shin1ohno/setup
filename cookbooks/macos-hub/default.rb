end

directory "#{home}/Library/LaunchAgents" do
  owner user
  mode "755"
end

file launchd_plist do
  owner user
  mode "644"
  content <<~PLIST
    <?xml version="1.0" encoding="UTF-8"?>
    <!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
    <plist version="1.0">
    <dict>
        <key>Label</key>
        <string>com.shin1ohno.macos-hub</string>
        <key>ProgramArguments</key>
        <array>
            <string>#{bundle_exec}</string>
            <string>#{home}/.config/macos-hub/macos-hub.toml</string>
        </array>
        <key>EnvironmentVariables</key>
        <dict>
            <key>RUST_LOG</key>
            <string>info</string>
            <key>PATH</key>
            <string>#{home}/.cargo/bin:/usr/local/bin:/usr/bin:/bin</string>
        </dict>
        <key>RunAtLoad</key>
        <true/>
